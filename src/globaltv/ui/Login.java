    private void submitBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtn1ActionPerformed
         UserModel userModel = null;
        if(username.getText().isEmpty() || password.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Nenhum campo pode estar em branco!");
        }else{
            userModel = globalTvController.getUserByUsername(username.getText());
            if(userModel == null){
                JOptionPane.showMessageDialog(this, "username nao existe!");
            }else{
                if(password.getText().equals(userModel.getPassword())){
                    globalTvController.setUser(userModel);
                    CardLayout cl = (CardLayout)contentPanel.getLayout();
                    cl.show(contentPanel, "channel");
                }else{
                    JOptionPane.showMessageDialog(this, "Password Incorreto!");
                }
            }
        }
    }//GEN-LAST:event_submitBtn1ActionPerformed


