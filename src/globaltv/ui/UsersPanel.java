    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        UserModel user = globalTvController.getUserByUsername((String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 0));
        
        Object[] options = {"fechar",
                     "deletar"};
        int del = JOptionPane.showOptionDialog(this, "deseja deletar o usuario "+ user.getUsername()+" ?" , "DELETAR USUARIO",
                JOptionPane.YES_NO_CANCEL_OPTION,
                 JOptionPane.PLAIN_MESSAGE,
                 null,
                 options,
                 null);
        
        
        if(del == 1){
            if(globalTvController.deleteUserModel(user.getUsername())){
                populate(globalTvController.listAllUsers());
                JOptionPane.showMessageDialog(contentPanel, "Usuario "+user.getUsername()+" deletado com sucesso!");
            }else{
                JOptionPane.showMessageDialog(contentPanel, "Ocorreu um erro ao deletar");
            }
            
        }
    }//GEN-LAST:event_jTable1MouseClicked


